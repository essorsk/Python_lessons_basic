# Задание-3:
# Напишите скрипт, заполняющий указанный файл (самостоятельно задайте имя файла)
# произвольными целыми цифрами, в результате в файле должно быть
# 2500-значное произвольное число.
# Найдите и выведите самую длинную последовательность одинаковых цифр
# в вышезаполненном файле.
print('\nЗадание-3: 2500 & maxlenght')
import os
import random
import re

name = 'hw04_2500'
new_path = os.path.join(name)
file = open(new_path,'w')
for i in range(0,2500):
    file.write(str(random.randint(0,9)))
file.close()

file = open(new_path,'r')
pattern = '1{2,2500}|2{2,2500}|3{2,2500}|4{2,2500}|5{2,2500}|6{2,2500}|7{2,2500}|8{2,2500}|9{2,2500}|0{2,2500}'
result = re.findall(pattern,file.readline())
file.close()

max = 0
length = ''
for j in result:
    if len(j) > max:
        length = j[:]
        max = len(j)
print('\nМаксимальная последовательность в 2500 файле: ',length)
