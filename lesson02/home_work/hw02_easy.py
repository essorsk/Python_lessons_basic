# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()
print('Задание 1. Вывод списка фруктов')
fruits = ['apple', 'kiwi', 'mango', 'shmango', 'lemon', 'papaya']
i = 0
while len(fruits) > i:
    print(i+1,'.','{:>12}'.format(fruits[i]))
    i += 1


