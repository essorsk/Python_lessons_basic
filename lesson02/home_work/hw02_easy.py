print('Задание 3. Манипуляция списком')
loto = [1, 24, 31, 4, 43, 51, 322, 21, 43, 34, 65, 76, 78, 87, 97]
i = 0
print('Исходное множество: ', loto)
while len(loto) > i:
    if loto[i] % 2 == 0:
        loto[i] = loto[i] / 4
    else:
        loto[i] = loto[i] * 2
    i += 1
